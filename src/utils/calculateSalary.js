

function calculateSalary(grade) {
    switch (grade) {
        case "1":
            return 93500;
        case "2":
            return 72000;
        case "3":
            return 33840;
        case "4":
            return 24125;
        case "5": 
            return 17500;
        case "6":
            return 15000
    }
}

export default calculateSalary;

